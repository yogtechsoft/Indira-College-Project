package com.model;

public class SupplierMaterialDetails {

	private String suppliername;
	private String materialName;
	private String Quantity;
	private String rate;
	private String transportCharge;
	private String totalAmount;
	private String date;
	private String sumTotalAmount;
	public String getSuppliername() {
		return suppliername;
	}
	public void setSuppliername(String suppliername) {
		this.suppliername = suppliername;
	}
	public String getMaterialName() {
		return materialName;
	}
	public void setMaterialName(String materialName) {
		this.materialName = materialName;
	}
	public String getQuantity() {
		return Quantity;
	}
	public void setQuantity(String quantity) {
		Quantity = quantity;
	}
	public String getRate() {
		return rate;
	}
	public void setRate(String rate) {
		this.rate = rate;
	}
	public String getTransportCharge() {
		return transportCharge;
	}
	public void setTransportCharge(String transportCharge) {
		this.transportCharge = transportCharge;
	}
	public String getTotalAmount() {
		return totalAmount;
	}
	public void setTotalAmount(String totalAmount) {
		this.totalAmount = totalAmount;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getSumTotalAmount() {
		return sumTotalAmount;
	}
	public void setSumTotalAmount(String sumTotalAmount) {
		this.sumTotalAmount = sumTotalAmount;
	}
	
	
	
}
